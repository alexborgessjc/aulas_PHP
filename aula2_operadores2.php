<?php
    //Declarando as variaveis
    $num1 = 1;
    $num2 = 2;   
    //Incremento
    $resultado1 = ++$num1;
    //Decremento
    $resultado2 = --$num2;
    //Mostrando os valores
    echo "O valor do incremento é: ".$resultado1."<br>";
    echo "O valor do decremento é: ".$resultado2."<br>";
?>